package gov.doc.isu.dwarf.taglib.displaytag.util;

/**
 * Constants class for tag attribute used in Dwarf tag library.
 * 
 * @author Steven Skinner JCCC
 */
public class DwarfTagConstants {
    /** Tag attribute class with equal sign and double quote <code>class=&quot</code> */
    public static final String CSS_CLASS = " class=\"";

    /** Tag attribute id with equal sign and double quote <code>id=&quot</code> */
    public static final String ID = " id=\"";

    /** Escaped Double quote sign <code>&quot</code> */
    public static final String DOUBLE_QUOTE = "\" ";

    /** Start of table tag <code>&lt;table</code>. */
    public static final String TABLE_START = "<table";

    /** End of table tag <code>&lt;/table&gt;</code>. */
    public static final String TABLE_END = "</table>";

    /** Start of label tag <code>&lt;label</code>. */
    public static final String LABEL_START = "<label";

    /** End of label tag <code>&lt;/label&gt;</code>. */
    public static final String LABEL_END = "</label>";

    /** Start of table row tag <code>&lt;tr&g</code>. */
    public static final String TR_START = "<tr>";
    /** End of table row tag <code>&lt;/tr&gt;</code>. */
    public static final String TR_END = "</tr>";
    /** Start of table column tag <code>&lt;td&g</code>. */
    public static final String TD_START = "<td>";
    /** End of table column tag <code>&lt;/td&gt;</code>. */
    public static final String TD_END = "</td>";

    /** end of tag <code>&gt;</code>. */
    public static final String CLOSE_TAG = ">";

    public static final String SPAN_START = "<span>";
    public static final String SPAN_END = "</span>";
    public static final String IMG_OPEN = "<img";
    public static final String IMG_END = "/>";
    public static final String SRC = " src=\"";
    public static final String TITLE = " title=\"";
    public static final String BORDER = " border=\"";
    public static final String HREF = " href=\"";

}
